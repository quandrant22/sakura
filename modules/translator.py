# ── Паттерны команд ────────────────────────────────────────────────

# "как по-японски X" / "как сказать X по-японски" / "переведи X на японский"
_JP_PATTERNS = [
    re.compile(r"как\s+(?:по-?японски|сказать)\s+(.+?)(?:\s*$)", re.I),
    re.compile(r"переведи\s+(.+?)\s+на\s+японск(?:ий|ую)", re.I),
    re.compile(r"как\s+будет\s+(.+?)\s+по-?японски", re.I),
]

# "что значит X" / "что означает X" (для японского/любого)
_MEANING_PATTERNS = [
    re.compile(r"что\s+(?:значит|означает)\s+(.+?)(?:\s*$)", re.I),
    re.compile(r"переведи\s+(.+?)(?:\s*$)", re.I),
]

# Японские символы
_JP_CHARS = re.compile(r"[\u3000-\u9fff\uf900-\ufaff]")


def is_translation_request(text: str) -> bool:
    """Проверяет, является ли текст запросом на перевод."""
    tl = text.lower().strip()
    for pat in _JP_PATTERNS + _MEANING_PATTERNS:
        if pat.search(tl):
            return True
    return False


def try_quick_translate(text: str) -> str | None:
    """
    Пытается перевести мгновенно из словаря.
    Возвращает форматированный ответ или None.
    """
    tl = text.lower().strip()

    # Проверяем японский текст — "что значит おはよう"
    for pat in _MEANING_PATTERNS:
        m = pat.search(tl)
        if m:
            phrase = m.group(1).strip().strip("\"'«»")
            # Ищем в словаре (точное совпадение)
            if phrase in _JP_COMMON:
                jp, romaji, ru = _JP_COMMON[phrase]
                return f"{jp} ({romaji}) — {ru}"
            # Ищем по частичному совпадению
            for key, (jp, romaji, ru) in _JP_COMMON.items():
                if phrase in key or key in phrase:
                    return f"{jp} ({romaji}) — {ru}"

    # Проверяем русский текст — "как по-японски привет"
    for pat in _JP_PATTERNS:
        m = pat.search(tl)
        if m:
            phrase = m.group(1).strip().strip("\"'«»")
            # Ищем по русскому переводу
            for key, (jp, romaji, ru) in _JP_COMMON.items():
                if phrase.lower() in ru.lower() or ru.lower() in phrase.lower():
                    return f"По-японски «{phrase}» — {jp} ({romaji})"

    return None


def build_translate_prompt(text: str) -> str:
    """Строит промпт для Gemini-перевода."""
    tl = text.lower().strip()

    # Определяем направление
    has_jp = bool(_JP_CHARS.search(text))

    if has_jp:
        return (
            f"Переведи японскую фразу/слово на русский: {text}\n"
            "Формат: японское (ромaji) — перевод\n"
            "Кратко, без пояснений."
        )
    else:
        return (
            f"Переведи на японский: {text}\n"
            "Формат: японское (ромaji) — перевод на русском\n"
            "Если есть несколько вариантов — укажи основной. Кратко."
        )
