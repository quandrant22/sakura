def get_time_context() -> str:
    now  = datetime.now()
    hour = now.hour
    if 6 <= hour < 12:    period = "утро"
    elif 12 <= hour < 17: period = "день"
    elif 17 <= hour < 22: period = "вечер"
    else:                  period = "ночь"
    return f"{now.strftime('%H:%M')}, {period}"
